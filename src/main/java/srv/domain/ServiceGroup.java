package srv.domain;

/**
 * @author Emma Driscoll
 * This class is a data holder for Service groups info. 
 *
 */
public class ServiceGroup {
	
	private String name;
	private String groupPres;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getGroupPres() {
		return groupPres;
	}
	public void setGroupPres(String groupPres) {
		this.groupPres = groupPres;
	}
	

}
